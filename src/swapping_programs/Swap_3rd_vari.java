package swapping_programs;

public class Swap_3rd_vari {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println("Before swap :");
		System.out.println("a : "+a );
		System.out.println("b : "+b );
		int c=0;
		c=a;
		a=b;
		b=c;
		System.out.println("After swap :");
		System.out.println("a : "+a );
		System.out.println("b : "+b );

	}

}
